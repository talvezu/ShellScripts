function usage() {
  echo "USAGE: ${0} dhcp eth_name"
  echo -e "USAGE ubuntu static:\n   ${0} static ip_address eth_name{default first non lo} <dns_nameserver>"
  echo -e "USAGE fedora static:\n   ${0} static ip_address <dns_nameserver>"
}

